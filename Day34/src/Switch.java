public class Switch {
    public static void main(String[] args) {

        String name = "Pasindu";

        switch (name){
            case "Yasindu":
                System.out.println("Yasindu");
            case "Pasindu":
                System.out.println("Pasindu");
            case "Kavindu":
                System.out.println("kavindu");
            default:
                System.out.println("no name");
        }

    }
}
